import type { NotarialData } from '@/types/notarial';

export function generateObjectionDocument(data: NotarialData): string {
  const currentDate = new Date().toLocaleDateString('ru-RU', {
    day: 'numeric',
    month: 'long',
    year: 'numeric',
  });

  const creditorDisplay = data.creditorName;

  const document = `Нотариусу: ${data.notaryName}
Лицензия № ${data.notaryLicense}

От: ${data.debtorName}
ИИН: ${data.debtorIIN}
Эл. почта: ${data.debtorEmail || '________________________'}


                              ВОЗРАЖЕНИЕ
              на исполнительную надпись нотариуса № ${data.enforcementNumber}

${data.enforcementDate} мною была получена исполнительная надпись № ${data.enforcementNumber} от ${data.enforcementDate}, согласно которой с меня подлежит взысканию:

    сумма задолженности:      ${data.debtAmount} тенге
    расходы нотариуса:        ${data.notaryExpenses} тенге
    общая сумма взыскания:    ${data.totalAmount} тенге

С исполнительной надписью я не согласен(-на) по следующим основаниям:


                              Основание №1:

В соответствии с пунктом 2 статьи 92-1 Закона Республики Казахстан «О нотариате», исполнительная надпись совершается при условии бесспорности требования взыскателя к должнику.

Однако данное требование не является бесспорным, поскольку:
  - досудебная претензия от взыскателя мною не была получена;
  - задолженность мною признана не была;
  - согласие на взыскание в бесспорном порядке мною не давалось.


                              Основание №2:

В соответствии с пунктом 3 статьи 92-1 Закона Республики Казахстан «О нотариате», исполнительная надпись не может быть совершена по требованиям о взыскании процентов и неустойки.

Вместе с тем, в сумму взыскания по исполнительной надписи незаконно включены:
  - проценты за пользование денежными средствами;
  - неустойка (пеня, штраф).

Данное обстоятельство является прямым нарушением вышеуказанной нормы закона.


На основании изложенного, ПРОШУ ВАС:

1. Отменить исполнительную надпись № ${data.enforcementNumber} от ${data.enforcementDate}.
2. Направить постановление об отмене исполнительной надписи на электронную почту: ${data.debtorEmail || '________________________'}.


________________________      ________________      ${currentDate}
        (ФИО)                    (Подпись)              (Дата)`;

  return document;
}
