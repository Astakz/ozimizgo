import type { NotarialData } from '@/types/notarial';

export function generateObjectionDocument(data: NotarialData): string {
  const currentDate = new Date().toLocaleDateString('ru-RU', {
    day: 'numeric',
    month: 'long',
    year: 'numeric',
  });

  const creditorDisplay = data.creditorName;

  const document = `                                                    Нотариусу ${data.notaryName}
                                                    Лицензия № ${data.notaryLicense}

                                                    от: ${data.debtorName}
                                                    ИИН ${data.debtorIIN}
                                                    Эл. почта: ${data.debtorEmail || '________________________'}


                                             ВОЗРАЖЕНИЕ
                    на исполнительную надпись нотариуса № ${data.enforcementNumber}

────────────────────────────────────────────────────────────────────────────────

${data.enforcementDate} года мной была получена исполнительная надпись № ${data.enforcementNumber} от ${data.enforcementDate} государственная лицензия № ${data.notaryLicense}.

При изучении исполнительной надписи мной было выяснено, что Вами была установлена сумма задолженности в пользу ${creditorDisplay}.

Сумма задолженности в размере ${data.debtAmount} тенге${data.debtAmountWords ? ` (${data.debtAmountWords})` : ''}.

Кроме того, подлежат взысканию в пользу указанного юридического лица расходы по совершению исполнительной надписи в сумме ${data.notaryExpenses} тенге.

Общая сумма, подлежащая взысканию, составляет ${data.totalAmount} тенге${data.totalAmountWords ? ` (${data.totalAmountWords})` : ''}.

────────────────────────────────────────────────────────────────────────────────

С исполнительной надписью я не согласен (-на) по основаниям:

    1. В соответствии с п.2 ст.92-1 Закона РК «О нотариате» на основании
       исполнительной надписи или соответствующего постановления производится
       взыскание задолженности по следующим бесспорным требованиям, в том числе
       об исполнении обязательства, основанного на письменной сделке, срок
       исполнения которой наступил и неисполнение обязательства признается
       должником, в том числе в ответе на претензию, направленную взыскателю
       в порядке досудебного урегулирования спора.

       В моем случае требование ${creditorDisplay} я не согласна с установленной
       суммой задолженности. Также мной не был получен досудебная претензия
       и неисполнение обязательства я не признавал (-а).

    2. Согласно п. 3 ст. 92-1 Закона на основании исполнительной надписи
       не производится взыскание неустоек (пеней), процентов.

       Однако, Вами учинена исполнительная надпись на сумму задолженности
       в размере ${data.totalAmount} тенге${data.totalAmountWords ? ` (${data.totalAmountWords})` : ''}, включив проценты
       (вознаграждение), неустойку (пени) в нарушение требований п. 3 ст. 92-1
       Закона.

────────────────────────────────────────────────────────────────────────────────

На основании выше сказанного,

                                             ПРОШУ ВАС:

Отменить учиненную Вами исполнительную надпись № ${data.enforcementNumber} от ${data.enforcementDate} государственная лицензия № ${data.notaryLicense} о взыскании суммы задолженности с меня в пользу ${creditorDisplay} в сумме ${data.totalAmount} тенге${data.totalAmountWords ? ` (${data.totalAmountWords})` : ''}.

Постановление об отмене исполнительной надписи направить на эл. почту:
${data.debtorEmail || '________________________'}

────────────────────────────────────────────────────────────────────────────────


${data.debtorName}                                                     _______________
                                                                          (подпись)

«____» _________________ ${new Date().getFullYear()} г.`;

  return document;
}
