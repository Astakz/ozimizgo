import type { NotarialData } from '@/types/notarial';

export function generateObjectionDocument(data: NotarialData): string {
  const currentDate = new Date().toLocaleDateString('ru-RU', {
    day: 'numeric',
    month: 'long',
    year: 'numeric',
  });

  const document = `
                                                    Нотариусу ${data.notaryName}
                                                    Лицензия № ${data.notaryLicense}
                                                    
                                                    От: ${data.debtorName}
                                                    ИИН: ${data.debtorIIN}


                                            ВОЗРАЖЕНИЕ
                        на исполнительную надпись нотариуса № ${data.enforcementNumber}


    Мною, ${data.debtorName}, ИИН ${data.debtorIIN}, получена исполнительная надпись нотариуса № ${data.enforcementNumber} от ${data.enforcementDate}, зарегистрированная в реестре за № ${data.registryNumber}, о взыскании в пользу ${data.creditorName}:

    - сумма задолженности: ${data.debtAmount} тенге;
    - расходы нотариуса: ${data.notaryExpenses} тенге;
    - общая сумма взыскания: ${data.totalAmount} тенге.

    Настоящим выражаю возражение против данной исполнительной надписи по следующим правовым основаниям:

    1. В соответствии с пунктом 2 статьи 92-1 Закона Республики Казахстан «О нотариате», исполнительная надпись нотариуса совершается только при наличии бесспорности требования взыскателя к должнику.

    Между тем, представленные взыскателем документы не подтверждают бесспорность требования. Наличие задолженности оспаривается мною, что исключает возможность совершения исполнительной надписи в порядке, установленном законодательством.

    2. В соответствии с пунктом 3 статьи 92-1 Закона Республики Казахстан «О нотариате», не допускается включение в исполнительную надпись сумм пени, неустойки, штрафов и процентов за пользование чужими денежными средствами, поскольку данные требования не относятся к бесспорным.

    Включение указанных сумм в исполнительную надпись является нарушением требований законодательства Республики Казахстан о нотариате.


                                            ПРОШУ ВАС:

    1. Рассмотреть настоящее возражение в установленном законом порядке.
    
    2. Отменить исполнительную надпись нотариуса № ${data.enforcementNumber} от ${data.enforcementDate} как совершённую с нарушением требований законодательства Республики Казахстан.

    3. Уведомить ${data.creditorName} о поступлении настоящего возражения.



    ${data.debtorName}

    Подпись: ____________________

    Дата: ${currentDate}
`.trim();

  return document;
}
